    //Lab 2 below method represents action listner for desynchronize button
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        File f =  new File("LDs//"+cl.getIdentifier());
        String[] options = f.list();
        JList deSyncList = new JList(options);
        //Below line referenced from: https://stackoverflow.com/questions/8899605/multiple-choices-from-a-joptionpane
        JOptionPane.showMessageDialog(null, deSyncList, "Choose directories to desync", JOptionPane.INFORMATION_MESSAGE);
        System.out.println("DeSync selected values:"+deSyncList.getSelectedValuesList().toString());
        
        boolean result = cl.desync(deSyncList.getSelectedValuesList());
        if(result){
            javax.swing.JOptionPane.showMessageDialog(null, "Desynchronization complete", "Information", JOptionPane.INFORMATION_MESSAGE);
        }else{
            javax.swing.JOptionPane.showMessageDialog(null, "Desynchronization failed", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

