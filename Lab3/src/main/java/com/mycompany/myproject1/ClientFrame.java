    //below method represents action listener for combo box.
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
       
        System.out.println("Combo box clicked.. value:"+(String)jComboBox1.getSelectedItem());
        String id = (String)jComboBox1.getSelectedItem();
        boolean res = cl.checkIfExists(id);
        if(res){ // if id exists pop us message to choose another id
            javax.swing.JOptionPane.showMessageDialog(null, "ID " +id+" already exists. Choose another ID", "Error", JOptionPane.ERROR_MESSAGE);
            identifierList.remove(id);
            jComboBox1.setModel(new DefaultComboBoxModel<>(identifierList.toArray(new String[identifierList.size()])));
            return;
        }
        else{
            jLabel3.setText("Identifier "+id);
            
            identifierList.remove(id);
            cl.addID(id);   //adding id to server side
            cl.setIdentifier(id);
            jComboBox1.setEnabled(false);
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

    //Lab 2 below method represents action listner for Synchronize button
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jList1.getSelectedValuesList().size() == 0){
            javax.swing.JOptionPane.showMessageDialog(null, "Please select the directories!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        System.out.println("JList selected values:"+jList1.getSelectedValuesList().toString());
        StringBuffer command = new StringBuffer("sync");
        List<String> l = jList1.getSelectedValuesList();
        for(String dir: l){
            command.append(" "+dir);
        }
        String result = cl.executeCommand(command.toString());
        if(result.equals("Synchronization complete!")){
            jTextArea2.setText(result);
            javax.swing.JOptionPane.showMessageDialog(null, "Synchronization complete!", "SYNC Status", JOptionPane.INFORMATION_MESSAGE);
        }
        //Client.hm.put(cl.getIdentifier(), new ArrayList(jList1.getSelectedValuesList()));
    }//GEN-LAST:event_jButton4ActionPerformed

